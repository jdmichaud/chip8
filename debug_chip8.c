#include <stdio.h>

#include "chip8.h"

void print_chip8(chip8_t *chip8) {
  fprintf(stderr, "V0: 0x%02x V8: 0x%02x \n", chip8->v[0x0], chip8->v[0x8]);
  fprintf(stderr, "V1: 0x%02x V9: 0x%02x \n", chip8->v[0x1], chip8->v[0x9]);
  fprintf(stderr, "V2: 0x%02x VA: 0x%02x \n", chip8->v[0x2], chip8->v[0xA]);
  fprintf(stderr, "V3: 0x%02x VB: 0x%02x \n", chip8->v[0x3], chip8->v[0xB]);
  fprintf(stderr, "V4: 0x%02x VC: 0x%02x \n", chip8->v[0x4], chip8->v[0xC]);
  fprintf(stderr, "V5: 0x%02x VD: 0x%02x \n", chip8->v[0x5], chip8->v[0xD]);
  fprintf(stderr, "V6: 0x%02x VE: 0x%02x \n", chip8->v[0x6], chip8->v[0xE]);
  fprintf(stderr, "V7: 0x%02x VF: 0x%02x \n", chip8->v[0x7], chip8->v[0xF]);

  fprintf(stderr, " I: 0x%04x PC: 0x%04x \n",  chip8->i, chip8->pc);
  fprintf(stderr, "DT: 0x%02x ST: 0x%02x \n", chip8->dt, chip8->st);
  fprintf(stderr, "SP: 0x%04x\n", chip8->sp);

  fprintf(stderr, "stack:\n");
  fprintf(stderr, "  &0x0000 0x%04x &0x0008 0x%04x \n", chip8->stack[0x0], chip8->stack[0x8]);
  fprintf(stderr, "  &0x0001 0x%04x &0x0009 0x%04x \n", chip8->stack[0x1], chip8->stack[0x9]);
  fprintf(stderr, "  &0x0002 0x%04x &0x000A 0x%04x \n", chip8->stack[0x2], chip8->stack[0xA]);
  fprintf(stderr, "  &0x0003 0x%04x &0x000B 0x%04x \n", chip8->stack[0x3], chip8->stack[0xB]);
  fprintf(stderr, "  &0x0004 0x%04x &0x000C 0x%04x \n", chip8->stack[0x4], chip8->stack[0xC]);
  fprintf(stderr, "  &0x0005 0x%04x &0x000D 0x%04x \n", chip8->stack[0x5], chip8->stack[0xD]);
  fprintf(stderr, "  &0x0006 0x%04x &0x000E 0x%04x \n", chip8->stack[0x6], chip8->stack[0xE]);
  fprintf(stderr, "  &0x0007 0x%04x &0x000F 0x%04x \n", chip8->stack[0x7], chip8->stack[0xF]);
}
